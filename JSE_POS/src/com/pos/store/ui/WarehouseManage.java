    private void initializeDataTable() {
        try {
            WarehouseList = WarehouseBuss.getAll();
            Object[][] data = null;
            if (WarehouseList != null && !WarehouseList.isEmpty()) {
                data = new Object[WarehouseList.size()][5];
                int row = 0;
                for (WareHouseDTO wh : WarehouseList) {
                    data[row][0] = wh.getCode();
                    data[row][1] = wh.getName();
                    data[row][2] = wh.getPhone();
                    data[row][3] = wh.getFax();
                    data[row++][4] = wh.getAddress();
                }
            }
            jTableWareHouse.setModel(new javax.swing.table.DefaultTableModel(data,
                    new String[]{"code", "Name", "Phone", "Fax", "Address"}));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

