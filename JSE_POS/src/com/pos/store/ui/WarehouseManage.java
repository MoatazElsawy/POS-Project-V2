    private void jButtonSaveWareHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveWareHActionPerformed
        // TODO add your handling code here:

        WareHouseDTO theWareHouse = new WareHouseDTO();

        try {

            boolean whIsOk = false;

            if (jTextFieldName.getText().trim().equals("")) {
                theWareHouse.setName(null);                                     // tha's if statement to check if feild null or not
                //JOptionPane.showMessageDialog(this, " Nmae is Empty");
                erroreMassageList.add("Nmae");
                whIsOk = false;
            } else {

                theWareHouse.setName(jTextFieldName.getText());
                whIsOk = true;
            }

            if (jTextFieldAddress.getText().trim().equals("")) {             // tha's if statement to check if feild null or not
                theWareHouse.setAddress(null);
                //JOptionPane.showMessageDialog(this, " Address is Empty");
                erroreMassageList.add("Address");
                whIsOk = false;
            } else {
                theWareHouse.setAddress(jTextFieldAddress.getText());
                whIsOk = true;
            }

            if (jTextFieldPhone.getText().trim().equals("")) {            // tha's if statement to check if feild null or not
                theWareHouse.setPhone(null);
                //JOptionPane.showMessageDialog(this, " Phone is Empty");
                erroreMassageList.add("Phone");
                whIsOk = false;

            } else {

                theWareHouse.setPhone(jTextFieldPhone.getText());
                whIsOk = true;
            }

            if (jTextFieldFax.getText().trim().equals("")) {                    // tha's if statement to check if feild null or not
                theWareHouse.setFax(null);
                //JOptionPane.showMessageDialog(this, " Fax is Empty");
                erroreMassageList.add("Fax");
                whIsOk = false;
            } else {

                theWareHouse.setFax(jTextFieldFax.getText());
                whIsOk = true;
            }
            
            

            if (whIsOk == true) {
                WarehouseBuss.save(theWareHouse);
                JOptionPane.showMessageDialog(this, "The WaeHouse Field has been Saved Successfully. ");
            } else {
                JOptionPane.showMessageDialog(this, "Saving Process Not Complete  ! \n" +erroreMassageList+ " Empty Field(s)\n"+"Try agin." ,"Error", JOptionPane.ERROR_MESSAGE);
            }

            erroreMassageList.removeAll(erroreMassageList);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());

        }

    }//GEN-LAST:event_jButtonSaveWareHActionPerformed

